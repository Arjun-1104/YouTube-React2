  export const generateRandomMessage =(length) => {
      let result = '';
      const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
      const charactersLength = characters.length;
      let counter = 0;
      while (counter < length) {
        result += characters.charAt(Math.floor(Math.random() * charactersLength));
        counter += 1;
      }
      return result;
  }

  export function formatviews(view) {
    if (view < 1000) {
      return view;
    } else if (view < 1000000) {
      return Math.floor(view / 100) / 10 + "K";
    } else if (view < 1000000000) {
      return Math.floor(view / 100000) / 10 + "M";
    } else {
      return Math.floor(view / 100000000) / 10 + "B";
    }
  }

  export function timeAgo(time) {
    const uploadTime = new Date(time);
    const now = new Date();
    const diff = Math.floor((now - uploadTime) / 1000);

    const units = [
      { label: "year", seconds: 31536000 },
      { label: "month", seconds: 2592000 },
      { label: "week", seconds: 604800 },
      { label: "day", seconds: 86400 },
      { label: "hour", seconds: 3600 },
      { label: "minute", seconds: 60 },
    ];

    for (const unit of units) {
      const count = Math.floor(diff / unit.seconds);
      if (count >= 1) {
        return `${count} ${unit.label}${count > 1 ? "s" : ""} ago`;
      }
    }
    return "just now";
  }